package module5;

public class AutoboxDemo {

	public static void main(String[] args) {
		// Autoboxing
		int a = 100;
		Integer i = a; // Convert Primitive Data Type to the Wrapper Class Object

		System.out.println("========================");
		// Unboxing
		int a1 = i; // Conver Wrapper class Object to Primitive Data Type

	}

}
